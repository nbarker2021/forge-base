def _rule30_simulate(max_depth: int):
    """Simulate Rule 30 from single-cell seed."""
    row: dict[int, int] = {0: 1}
    center_bits: list[int] = []
    local_states: list[tuple[int, int, int]] = []
    for _ in range(max_depth):
        L = row.get(-1, 0)
        C = row.get(0, 0)
        R = row.get(1, 0)
        local_states.append((L, C, R))
        new_row: dict[int, int] = {}
        positions: set[int] = set()
        for x in row:
            positions.update([x - 1, x, x + 1])
        for x in positions:
            l = row.get(x - 1, 0)
            c = row.get(x, 0)
            r = row.get(x + 1, 0)
            bit_index = (l << 2) | (c << 1) | r
            if (30 >> bit_index) & 1:
                new_row[x] = 1
        row = new_row
        center_bits.append(row.get(0, 0))
    return center_bits, local_states


def t_emission(L: int, C: int, R: int) -> int:
    """T_EMISSION (Theorem A): proven O(1), 0 defects."""
    return (1 - L) if C == 1 else (L ^ R)


# ---------------------------------------------------------------------------
# Main predictor: predict EVERY bit from 1 to N simultaneously
# ---------------------------------------------------------------------------

def predict_sequence(N: int, verbose: bool = True) -> dict[str, Any]:
    """
    Predict EVERY bit from 1 to N simultaneously using the oloid bead string.

    This is NOT predicting bit N from bits 1..N-1.
    This is generating the COMPLETE oloid from the known history and using
    it to validate all N bits simultaneously AND predict bit N+1.

    The oloid consistency check: if the generated oloid is valid
    (all contact points are at the correct Lie conjugate states,
    all arch heights are consistent with the fold structure),
    then every bit in the sequence is verified simultaneously.

    Returns:
      - oracle_accuracy: always 1.0 (T_EMISSION proven)
      - oloid_accuracy: fraction of bits correctly predicted by arch structure
      - consistency: fraction of contact points on the correct circle
      - prediction: the predicted next bit at depth N+1
    """
    center_bits, local_states = _rule30_simulate(N + 1)

    # Build the oloid from the known history (depths 1..N)
    bead_string = OloidBeadString(head_state=local_states[0])

    oracle_correct = 0
    oloid_correct = 0
    consistency_correct = 0
    contact_count = 0

    predictions = []

    for depth in range(1, N + 1):
        s = local_states[depth - 1]
        oracle_bit = center_bits[depth - 1]

        # Get the oloid prediction BEFORE consuming this state
        pred = bead_string.predict_next_bit()
        predicted_bit = pred["predicted_bit"]

        # Oracle: T_EMISSION on the true state (always correct)
        oracle_bit_check = t_emission(*s)
        oracle_correct += int(oracle_bit_check == oracle_bit)

        # Oloid prediction
        oloid_correct += int(predicted_bit == oracle_bit)

        # Consistency: contact points should be on the correct circle
        in_contact = s in CONTACT_STATES
        if in_contact:
            contact_count += 1
            # Check: is this contact on the expected active circle?
            expected_circle = bead_string.active_circle
            on_f = s in CIRCLE_F
            on_p = s in CIRCLE_P
            on_expected = (on_f and expected_circle == 'F') or \
                          (on_p and expected_circle == 'P')
            consistency_correct += int(on_expected)

        # Consume the state into the bead string
        record = bead_string.consume(s)
        record["oracle_bit"] = oracle_bit
        record["predicted_bit"] = predicted_bit
        record["oracle_match"] = (oracle_bit_check == oracle_bit)
        record["oloid_match"] = (predicted_bit == oracle_bit)
        predictions.append(record)

    # Predict bit N+1 (the future)
    next_pred = bead_string.predict_next_bit()

    if verbose:
        _print_sequence_report(predictions, next_pred, N,
                               oracle_correct, oloid_correct,
                               consistency_correct, contact_count)

    return {
        "N":                N,
        "oracle_accuracy":  oracle_correct / N,
        "oloid_accuracy":   oloid_correct / N,
        "consistency":      consistency_correct / contact_count if contact_count > 0 else None,
        "contact_count":    contact_count,
        "fold_count":       bead_string.fold_count,
        "final_arch_height": bead_string.arch_height,
        "next_prediction":  next_pred,
        "head_state":       bead_string.head,
        "tail_state":       local_states[N - 1],
        "predictions":      predictions,
    }


def _print_sequence_report(predictions, next_pred, N,
                           oracle_correct, oloid_correct,
                           consistency_correct, contact_count):
    print(f"\n{'='*70}")
    print(f"OLOID BEAD-STRING PREDICTOR  —  sequence N=1..{N}")
    print(f"{'='*70}")
    print(f"\nHead (anchor): {predictions[0]['state']}  "
          f"Tail (current): {predictions[-1]['state']}")
    print(f"The tether never leaves.\n")

    print(f"{'N':4s} | {'state':12s} | {'arc_h':5s} | {'circ':5s} | "
          f"{'pred':4s} | {'bit':3s} | {'match':5s} | {'note'}")
    print(f"{'-'*70}")
    for i, r in enumerate(predictions[:50]):
        N_i = i + 1
        note = ""
        if r["in_contact"]:
            circle = "F" if r["state"] in CIRCLE_F else "P"
            note = f"CONTACT({circle})"
        if r["fold_occurred"]:
            note += " FOLD"
        match = "OK" if r["oloid_match"] else "MISS"
        print(f"{N_i:4d} | {str(r['state']):12s} | {r['arch_height']:5d} | "
              f"{r['active_circle']:5s} | {r['predicted_bit']:4d} | "
              f"{r['oracle_bit']:3d} | {match:5s} | {note}")

    if N > 50:
        print(f"  ... ({N - 50} more depths)")

    print(f"\n{'='*70}")
    print(f"SUMMARY")
    print(f"{'='*70}")
    print(f"  Oracle accuracy (T_EMISSION):  {oracle_correct}/{N} = {oracle_correct/N:.4f}")
    print(f"  Oloid accuracy (arch+circle):  {oloid_correct}/{N} = {oloid_correct/N:.4f}")
    print(f"  Circle consistency:            {consistency_correct}/{contact_count} = "
          f"{consistency_correct/contact_count:.4f}" if contact_count > 0 else "  Circle consistency: N/A")
    print(f"  Contact points (Lie conj):     {contact_count}/{N} = {contact_count/N:.4f}")
    print(f"  Fold events (K-boundaries):    {next_pred['fold_count']}")
    print(f"  Final arch height:             {next_pred['arch_height']}")
    print(f"\n  NEXT BIT PREDICTION (N+1={N+1}):")
    print(f"    Predicted bit:    {next_pred['predicted_bit']}")
    print(f"    Confidence:       {next_pred['confidence']:.3f}")
    print(f"    Method:           {next_pred['method']}")
    print(f"    Active circle:    {next_pred['active_circle']}")
    print(f"    Predicted contact: {next_pred['predicted_contact']}")
    print(f"{'='*70}\n")


