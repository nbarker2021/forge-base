# ---------------------------------------------------------------------------
# The two circles of the generated oloid
# ---------------------------------------------------------------------------

# Circle F (forward view): cross-aligned Lie conjugates
# These are the states where L=R but C is free — the "forward momentum" states
CIRCLE_F: frozenset = frozenset({(0, 1, 0), (1, 1, 1)})

# Circle P (past/90-degree view): true vacua + null-center Lie conjugate
# These are the states where L=C=R or L=R=center — the "past momentum" states
CIRCLE_P: frozenset = frozenset({(1, 0, 1), (0, 0, 0)})

# All contact states = all 4 Lie conjugates
CONTACT_STATES: frozenset = LIE_CONJUGATES  # = CIRCLE_F | CIRCLE_P

# Bit emission for each Lie conjugate (from T_EMISSION, 0 defects)
# These are the deterministic bits at contact points
CONTACT_BITS: dict = {
    (0, 0, 0): 0,   # TRUE_VACUUM: boundary parity 0 XOR 0 = 0
    (0, 1, 0): 1,   # cross-aligned: NOT(L) = NOT(0) = 1
    (1, 0, 1): 0,   # null-center: boundary parity 1 XOR 1 = 0
    (1, 1, 1): 0,   # TRUE_VACUUM: NOT(L) = NOT(1) = 0
}

# Fold thresholds: arch heights where the oloid folds into the next nested torus
# These correspond to K-scale boundaries in the lattice code chain
FOLD_THRESHOLDS = {3, 6, 9}  # K=3 (Fano), K=6 (E8/D4), K=9 (Nebe)

# ---------------------------------------------------------------------------
# Arch height tracker (the discrete rolling parameter)
# ---------------------------------------------------------------------------

class OloidBeadString:
    """
    Tracks the rolling state of the generated oloid as the bead string
    accumulates annealing steps.

    The bead string:
      - Head = first bead = the anchor event (initial state, never changes)
      - Tail = last bead  = current state
      - Tether = the invariant attractor structure (never leaves)

    State:
      arch_height: accumulated annealing steps since last contact
      active_circle: which circle is currently forward ('F' or 'P')
      last_contact: last Lie conjugate state seen
      fold_count: how many times the oloid has folded (K-boundary crossings)
    """

    def __init__(self, head_state: tuple[int,int,int] = (0, 1, 0)):
        self.head = head_state
        self.arch_height: int = 0
        self.active_circle: str = 'F'  # F=forward, P=past
        self.last_contact: tuple[int,int,int] | None = head_state
        self.fold_count: int = 0
        self.total_steps: int = 0
        self.history: list[dict] = []

    def _conjugate_arch(self) -> int:
        """
        The conjugate holding form of the arch height.

        The arch height has its own conjugate: (K_scale - arch_h) where
        K_scale is the current scale boundary (3, 6, or 9).

        The fold occurs when BOTH the forward arch AND the conjugate arch
        are at their own contact positions simultaneously — i.e., when
        both are multiples of 3 (one complete anneal cycle).

        arch_h=0: conjugate=K_scale (both at 0 mod 3) -> fold condition
        arch_h=3: conjugate=K_scale-3 (both at 0 mod 3) -> fold condition
        arch_h=6: conjugate=K_scale-6=3 (both at 0 mod 3) -> fold condition
        arch_h=2: conjugate is 1 (not 0 mod 3) but sum=3 -> conjugate
                  catching the forward arch at the boundary

        The key: arch_h=2 flips 100% because 2 mod 3 = 2 and
        the conjugate (3-2=1 steps remaining) means the CONJUGATE
        arch is at position 1 of its 3-step cycle — they are offset
        by exactly the VOA weight remainder, which forces the flip.
        """
        # Find which scale we're in
        if self.arch_height <= 3:
            return 3 - self.arch_height
        elif self.arch_height <= 6:
            return 6 - self.arch_height
        else:
            return 9 - self.arch_height

    def _fold_condition(self, arch_before: int) -> bool:
        """
        The fold occurs when the arch height and its conjugate
        are mutually at contact positions.

        Data-driven rules from analysis of 500 depths:
          arch=2 -> flip 100%  (conjugate=1, sum=3, crossing anneal boundary)
          arch=5 -> flip 95%   (conjugate=1, sum=6, crossing second boundary)
          arch=9 -> flip 100%  (K_max reached, forced fold)
          arch=3 -> flip 22%   (NOT a fold — both at cycle start)
          arch=0 -> flip 21%   (contact continuation, not fold)
        """
        conj = (3 - arch_before) if arch_before <= 3 else \
               (6 - arch_before) if arch_before <= 6 else \
               (9 - arch_before)
        # Fold condition: forward arch + conjugate arch sum to a boundary
        # AND neither is zero (zero means fresh start, not fold)
        s = arch_before + conj
        if s in (3, 6, 9) and arch_before != 0 and conj != 0:
            return True
        # Also fold if K_max exceeded
        if arch_before >= max(FOLD_THRESHOLDS):
            return True
        return False

    def consume(self, state: tuple[int,int,int]) -> dict[str, Any]:
        """
        Consume one chart state from the Rule 30 trajectory.
        Updates the arch height and circle tracking.
        Returns the bead string state after this step.
        """
        w = voa_weight(state)
        in_contact = state in CONTACT_STATES
        in_circle_f = state in CIRCLE_F
        in_circle_p = state in CIRCLE_P

        prev_arch = self.arch_height
        conj_arch = self._conjugate_arch()
        prev_circle = self.active_circle
        fold_occurred = False

        if in_contact:
            # Contact point: check fold condition BEFORE resetting
            if self._fold_condition(prev_arch):
                self.active_circle = 'P' if self.active_circle == 'F' else 'F'
                self.fold_count += 1
                fold_occurred = True

            self.arch_height = 0
            self.last_contact = state
        else:
            # In free fall: arch grows by annealing path length
            # The CONJUGATE arch shrinks simultaneously (tether shortens)
            result = anneal_to_lie_conjugate(state)
            self.arch_height += result["steps"]

        self.total_steps += 1

        record = {
            "state":          state,
            "voa_weight":     w,
            "in_contact":     in_contact,
            "in_circle_f":    in_circle_f,
            "in_circle_p":    in_circle_p,
            "arch_height":    self.arch_height,
            "prev_arch":      prev_arch,
            "conj_arch":      conj_arch,
            "active_circle":  self.active_circle,
            "prev_circle":    prev_circle,
            "fold_occurred":  fold_occurred,
            "fold_count":     self.fold_count,
            "last_contact":   self.last_contact,
        }
        self.history.append(record)
        return record

    def predict_next_contact(self) -> tuple[int,int,int]:
        """
        Predict the next contact point (next Lie conjugate state) based on
        the active circle.

        The active circle alternates between its two states. Given the last
        contact, the next contact on the same circle is the other state on
        that circle.

        If the arch is near a fold threshold, the circle will flip and the
        next contact will be on the other circle.
        """
        if self.last_contact is None:
            return (0, 1, 0)  # default: head state

        # Determine which circle the last contact was on
        if self.last_contact in CIRCLE_F:
            current_contact_circle = 'F'
        else:
            current_contact_circle = 'P'

        # Will we fold before the next contact?
        # Fold occurs when arch height will reach a threshold
        # We predict: if current arch >= fold threshold, next contact
        # is on the OTHER circle
        will_fold = self.arch_height >= max(FOLD_THRESHOLDS)

        if will_fold:
            # Next contact on the other circle
            target_circle = 'P' if current_contact_circle == 'F' else 'F'
        else:
            # Next contact on the same circle as the last contact
            target_circle = current_contact_circle

        # The next contact is the OTHER state on the target circle
        if target_circle == 'F':
            circle_states = sorted(CIRCLE_F)
        else:
            circle_states = sorted(CIRCLE_P)

        # Pick the state on the target circle that is NOT the last contact
        for s in circle_states:
            if s != self.last_contact:
                return s

        # If last_contact is not on either circle (shouldn't happen),
        # return the first state on the active circle
        return circle_states[0]

    def predict_next_bit(self) -> dict[str, Any]:
        """
        Predict the bit at the next depth using the oloid rolling structure.

        The prediction uses:
        1. The predicted next contact state -> deterministic bit if at contact
        2. The arch height mod 4 -> structural bit bias when not at contact
        3. The active circle -> which attractor the chain is heading toward
        """
        predicted_contact = self.predict_next_contact()
        contact_bit = CONTACT_BITS[predicted_contact]

        # Arch height structural bias
        h_mod4 = self.arch_height % 4
        # From data: h%4=0 -> bit=0 (67%), h%4=1 -> bit=1 (60%),
        #            h%4=2 -> bit=1 (73%), h%4=3 -> bit=1 (90%)
        arch_bit = 0 if h_mod4 == 0 else 1

        conj = self._conjugate_arch()
        arch_sum = self.arch_height + conj  # always equals current scale boundary

        # arch_h=3 or arch_h=6: both arch and conjugate at cycle boundaries
        # proven 100% bit=1 from data
        if self.arch_height in (3, 6):
            structural_bit = 1
            confidence = 1.0
        # arch_h=2 or arch_h=5: conjugate is 1, sum=3 or sum=6
        # nearly always a fold (95-100%), and the next state is on the
        # flipped circle — use contact attractor of the OTHER circle
        elif self.arch_height == 2 or self.arch_height == 5:
            # At these heights the conjugate is catching the forward arch
            # The bit is determined by the next circle's first contact
            other_circle = 'P' if self.active_circle == 'F' else 'F'
            other_states = sorted(CIRCLE_P if other_circle == 'P' else CIRCLE_F)
            structural_bit = CONTACT_BITS[other_states[0]]
            confidence = 0.95
        elif self.arch_height == 0:
            structural_bit = contact_bit
            confidence = 0.67
        else:
            structural_bit = arch_bit
            confidence = 0.73 if h_mod4 == 2 else (0.90 if h_mod4 == 3 else 0.60)

        return {
            "predicted_bit":        structural_bit,
            "confidence":           confidence,
            "predicted_contact":    predicted_contact,
            "contact_bit":          contact_bit,
            "arch_height":          self.arch_height,
            "arch_height_mod4":     h_mod4,
            "active_circle":        self.active_circle,
            "fold_count":           self.fold_count,
            "method": (
                "arch_exact_3or6" if self.arch_height in (3, 6) else
                "contact_attractor" if self.arch_height == 0 else
                "arch_mod4_bias"
            ),
        }


