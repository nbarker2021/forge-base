"""
d12_action.py — D12 as the Jordanian envelope of D4

D12 is the dihedral group of order 12 (symmetries of the regular hexagon):
  6 rotations: r^k for k in {0,1,2,3,4,5}
  6 reflections: s * r^k for k in {0,1,2,3,4,5}

In this framework D12 is the group that acts on D4 itself — meaning D12 is
the outer automorphism envelope of D4 extended by the ℤ/3 color-cyclic action.

Structural decomposition:
  D4  = {e, r², r, r³, s, sr, sr², sr³}   (8 elements, chart's antipodal block)
  ℤ/3 = {0, 1, 2}                          (S3 color cyclic: C-, C0, C+ rotation)
  D12 = D4 ⋊ ℤ/3  (semidirect product via the outer ℤ/3 automorphism of D4)

The outer ℤ/3 automorphism of D4 acts by cyclically permuting the three
non-central involutions of D4 — exactly the three trace-2 idempotents
{C-, C0, C+} in the J_3(O) shell=2 stratum.  D4's three axis-pairs {1,2,3}
correspond to the S3 transpositions {(12),(13),(23)}, and ℤ/3 cycles them.

Therefore:
  D12 is the Jordanian that encodes D4:
    — D4 carries the local Weyl/spin/sheet action on the 8 chart states
    — ℤ/3 (the color rotation inside S3) acts on D4's reflection classes
    — Together they span the D12 orbit on the 12 distinct D4 coset states

The conjugate transfer inside D12:
  For each of the 12 D12 generators g, conjugation φ_g: x ↦ g·x·g⁻¹
  restricted to the 3-element trace-2 stratum is a permutation in S3.
  This is exactly the same A3-spine action as the D4→J30 bridge.
  D12 therefore performs the same conjugate transfer inside the broader
  12-element span that D4 performs on the 8-element chart — the idempotent
  chain continues one level up.

Idempotent chain summary:
  J_3(O) trace-2 idempotents  ←  (idempotent to D4 action)
  D4 chart states              ←  (idempotent to D12 conjugation)
  D12 orbit                    →  (new boundary for the next lift)
"""

