# ---------------------------------------------------------------------------
# D12 element representation: (rotation_k, is_reflection)
# rotation_k in {0,1,2,3,4,5}, is_reflection in {0,1}
# multiplication law for Dih(6): (k, s) · (l, t) = (k + (-1)^s * l mod 6, s XOR t)
# ---------------------------------------------------------------------------

D12_ELEMENTS: list[tuple[int, int]] = [
    (k, r) for r in (0, 1) for k in range(6)
]

D12_NAMES: dict[tuple[int, int], str] = {
    (0, 0): "e",
    (1, 0): "r",
    (2, 0): "r²",
    (3, 0): "r³",
    (4, 0): "r⁴",
    (5, 0): "r⁵",
    (0, 1): "s",
    (1, 1): "sr",
    (2, 1): "sr²",
    (3, 1): "sr³",
    (4, 1): "sr⁴",
    (5, 1): "sr⁵",
}


def d12_multiply(a: tuple[int, int], b: tuple[int, int]) -> tuple[int, int]:
    """Group multiplication in D12 = Dih(6)."""
    k, s = a
    l, t = b
    sign = -1 if s else 1
    return ((k + sign * l) % 6, s ^ t)


def d12_inverse(a: tuple[int, int]) -> tuple[int, int]:
    """Group inverse in D12."""
    k, s = a
    if s == 0:
        return ((-k) % 6, 0)
    else:
        return (k, 1)


def d12_conjugate(g: tuple[int, int], x: tuple[int, int]) -> tuple[int, int]:
    """Conjugation: g · x · g⁻¹ in D12."""
    return d12_multiply(d12_multiply(g, x), d12_inverse(g))


