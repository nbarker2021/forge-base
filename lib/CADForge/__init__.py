class CADForgeBuilder:
    def __init__(self, family: str, name: str = "design"):
        if family not in FAMILIES:
            raise WireBlockError(f"unknown family {family!r}; have {sorted(FAMILIES)}")
        f = FAMILIES[family]
        self.family = family
        self.name = name
        self.wb = WireBlock(f["base_template"], f["nodes"], f["edges"],
                            f["allowed_attachables"], f["variables"])
        self._n = 0

    def tweak(self, var: str, value) -> "CADForgeBuilder":
        self.wb.set_var(var, value)
        return self

    def attach(self, attachable: str, anchor_node: str, template: str | None = None,
               scale: float = 1.0, orientation: str = "external") -> "CADForgeBuilder":
        self.wb.attach(f"att{self._n}", attachable, anchor_node, template, scale, orientation)
        self._n += 1
        return self

    def receipt(self) -> dict:
        r = self.wb.receipt(self.name)
        r["family"] = self.family
        return r


def to_solidworks_bas(receipt: dict) -> str:
    """Export a SolidWorks VBA macro that lays the legal wire skeleton."""
    lines = [
        "Option Explicit",
        "' Generated by CADForge / WireBlock.",
        "' SolidWorks remains the CAD system; this macro imports the legal wire skeleton.",
        "Dim swApp As SldWorks.SldWorks",
        "Dim swModel As SldWorks.ModelDoc2",
        "Sub CADForgeWireBlockImport()",
        "    Set swApp = Application.SldWorks",
        "    Set swModel = swApp.NewPart",
    ]
    pos = {n["id"]: (n.get("x", 0), n.get("y", 0), n.get("z", 0)) for n in receipt["nodes"]}
    for e in receipt["edges"]:
        a, b = pos.get(e["source"], (0, 0, 0)), pos.get(e["target"], (0, 0, 0))
        lines.append(f"    swModel.SketchManager.CreateLine "
                     f"{a[0]/1000.0}, {a[1]/1000.0}, {a[2]/1000.0}, "
                     f"{b[0]/1000.0}, {b[1]/1000.0}, {b[2]/1000.0}")
    lines.append("End Sub")
    return "\n".join(lines)


def verify() -> dict:
    b = CADForgeBuilder("panel_bracket", name="demo_panel_bracket")
    b.tweak("width_mm", 96).attach("rib", anchor_node="base:n0")
    r = b.receipt()
    bas = to_solidworks_bas(r)
    return {"forge": "CADForge", "status": "pass" if r["legal"] else "fail",
            "family": r["family"], "design_id": r["design_id"],
            "attachables": len(r["attachables"]), "solidworks_bas_lines": bas.count("\n") + 1}
