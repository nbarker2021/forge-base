"""CADForge - the CAD-facing Forge product (initialized-wireframe CAD).

Documented in the CADForge+WireBlock promotion slice (2026-06-11) but never
implemented as a module. Built here as a real, importable, stdlib-only builder
matching the documented API:

    from CADForge import CADForgeBuilder
    b = CADForgeBuilder("panel_bracket", name="demo")
    b.tweak("width_mm", 96)
    b.attach("rib", anchor_node="base:n0")
    receipt = b.receipt()

CADForge supplies a legal wireframe family; the user assembles from allowed
attachables instead of drawing arbitrary geometry. WireBlock enforces legality;
`to_solidworks_bas` exports a VBA macro from the receipt (SolidWorks remains the
CAD system). Downstream STEP/STL/BOM/toolpath exporters remain open obligations.
"""
from __future__ import annotations

from WireBlock import WireBlock, WireBlockError  # noqa: F401

